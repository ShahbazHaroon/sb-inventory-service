package com.ubaidsample.inventory.repository;

import com.ubaidsample.inventory.entity.Category;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import java.util.Optional;

@Repository
public interface CategoryRepository extends JpaRepository<Category, Long> {

    Optional<Category> findByName(String name);

    Optional<Category> findBySlug(String slug);

    // Helps avoid duplicate categories under the same parent
    Optional<Category> findByNameAndParentCategory(String name, Category parentCategory);
}